public class Account{
    /** The name */
    private String _name;
    /** The count. */
    private float _count;

    /** Get the name */
    public String GetName()
    {
        return _name;
    }

    /** Set the name */
    public void SetName(String name)
    {
        this._name = name;
    }

    /** Get the count. */
    public float GetCount()
    {
        return this._count;
    }
 
    /** Set the count */
    public void SetCount(float count)
    {
        this._count = count;
    }
    
    
}